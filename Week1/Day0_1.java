public class Day0_1 {
    static void printArr(int[] arr){
        //for each loop
        for(int i:arr)
            System.out.print(i+" ");
    }
    // Return the sum of your arr: 1,2,3,4,5
    // Print an array in reverse order: 5,4,3,2,1
    // Reverse the array(optional)
    public static void main(String[] args) {
        int[] arr= {1,2,3,4,5};
        printArr(arr);
        System.out.println();
        System.out.println();
        System.out.println();
        System.out.println();
    }
}
